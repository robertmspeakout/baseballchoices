// ── Roster size and scholarship info ──
// D1 baseball: 11.7 scholarships, typical roster 35-40
// D2: 9.0 scholarships, roster ~30-35
// D3: 0 athletic scholarships, roster ~30-40
// JUCO: 24 scholarships, roster ~25-35
function getRosterInfo(school) {
  switch (school.division) {
    case "D1": return { roster_size: 35, scholarship_limit: 11.7 };
    case "D2": return { roster_size: 32, scholarship_limit: 9.0 };
    case "D3": return { roster_size: 35, scholarship_limit: 0 };
    case "JUCO": return { roster_size: 30, scholarship_limit: 24.0 };
    default: return { roster_size: 35, scholarship_limit: 11.7 };
  }
}

